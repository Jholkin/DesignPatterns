<?php

namespace App\Adapter\APIBankY;

class YBankCreditApprove
{
    public $name;
    public $credit;

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getCredit()
    {
        return $this->credit;
    }

    public function setCredit($credit)
    {
        $this->credit = $credit;
    }
}